package playground.trees;

public class TreeNode2Result {

    private TreeNode2 node;
    private int index;

    public TreeNode2Result(TreeNode2 node, int index) {
        this.node = node;
        this.index = index;
    }

    public TreeNode2 getNode() {
        return node;
    }

    public int getIndex() {
        return index;
    }

}
