package com.amazon.test;


public class Athlete 
{
		private int mass;
		

		private int strength;

		public Athlete(int mass, int strength)
		{
			this.mass = mass;
			this.strength = strength;
		}
		
		public int getMass() 
		{
			return mass;
		}

		public int getStrength() 
		{
			return strength;
		}
		
		

		
}
